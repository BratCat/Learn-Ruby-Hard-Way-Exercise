def pets(cats, dogs)
    puts "She has #{cats} cats!"
    puts "He has #{dogs} dogs!"
    puts "If they get anymore pets they will need a bigger place!"
end

pets(10, 5)

number_cats = 20
number_dogs = 4

pets(number_cats, number_dogs)

